function createPacBoard1() {
    if (!grid) {
        console.error('El elemento grid no existe');
        return;
    }

    for (let i = 0; i < layout1.length; i++) {
        const square = document.createElement("div");
        grid.appendChild(square);
        squares.push(square);

        // añadiendo las clases según el layout
        if (layout1[i] === 0) {
            square.classList.add("pac-dot");
        } else if (layout1[i] === 1) {
            square.classList.add("wall");
        } else if (layout1[i] === 3) {
            square.classList.add("power-pellet");
        }
        //Solución 1       
        else if (layout1[i] == 4) {
            square.classList.add("selector-separacion");
        }

    }
}

createPacBoard1();

let pacmanCurrentIndex1 = 490;
squares[pacmanCurrentIndex1].classList.add("pacman");

function control1(e) {
    squares[pacmanCurrentIndex1].classList.remove("pacman");

    switch (e.keyCode) {
        case 37://izquierda
            if (pacmanCurrentIndex1 % width !== 0 && !squares[pacmanCurrentIndex1 - 1].classList.contains('wall'))
                pacmanCurrentIndex1 -= 1;
            break;
        case 38://arriba
            if (pacmanCurrentIndex1 - width >= 0 && !squares[pacmanCurrentIndex1 - width].classList.contains('wall'))
                pacmanCurrentIndex1 -= width;
            break;
        case 39://derecha
            if (pacmanCurrentIndex1 % width < width - 1 && !squares[pacmanCurrentIndex1 + 1].classList.contains('wall'))
                pacmanCurrentIndex1 += 1;
            break;
        case 40://abajo
            if (pacmanCurrentIndex1 + width < width * width && !squares[pacmanCurrentIndex1 + width].classList.contains('wall'))
                pacmanCurrentIndex1 += width;
            break;
    }

    squares[pacmanCurrentIndex1].classList.add("pacman");

    pacDotEaten1();
}

document.addEventListener("keyup", control1);

function pacDotEaten1() {
    if (squares[pacmanCurrentIndex1].classList.contains('pac-dot')) {
        score++;
        scoreDisplay.textContent = score;
        squares[pacmanCurrentIndex1].classList.remove('pac-dot');
    }
}






